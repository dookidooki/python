
s = "파이썬 너무 좋아요"

# 인덱싱 : 특정 위치값을 지정해서 거기 있는 데이터만 출력하기
print(s[0])
print(s[4])

# 슬라이싱 : 한번에 연속적으로 여러 글자를 골라내기
print(s[0:4])
print(s[7:])
print(s[:8])

